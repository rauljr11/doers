<script type="text/javascript">
	$(document).on('ready', function(e){
		var regForm = $('form[name="registration"]');
		var logForm = $('form[name="login"]');
		var empty = false;
		$('#regSubmit', regForm).on('click', function(e) {

			regForm.find('input').each(function(e){
				if ($(this).val() == '') {
					empty = true;
					$('.message', regForm).html($(this).attr('id') + ' must not me empty');
					return false;
				}
			});

			if (empty) {
				return false;
			}

			$.ajax({
				url: '/users/checkInfo/'+$('#email', regForm).val()+'/'+$('#username', regForm).val(),
				type: 'GET',
				success: function (data) {
					var res = JSON.parse(data);
					if (res.email.taken) {
						$('.message', regForm).html(res.email.message);
						return false;
					} else if (res.username.taken) {
						$('.message', regForm).html(res.username.message);
						return false;
					} else if (!/^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/.test($('#email', regForm).val())) {
						$('.message', regForm).html('Invalid Email');
					} else {
						var pass1 = $('#password', regForm).val();
						var pass2 = $('#re-password', regForm).val();
						if (pass1 != pass2) {
							$('.message', regForm).html('Password not match');
							return false;
						} else {
							regForm.submit();	
						}
					}
				},
				error: function (error) {
					return false;
				}
			});

			return false;
		});

		$('#loginSubmit', logForm).on('click', function(e){
			logForm.find('input').each(function(e){
				if ($(this).val() == '') {
					empty = true;
					$('.message', logForm).html($(this).attr('id') + ' must not me empty');
					return false;
				}
			});

			if (empty) {
				return false;
			}

			$.ajax({
				url: '/users/checklogin/'+$('#email', logForm).val()+'/'+$('#password', logForm).val(),
				type: 'GET',
				success: function (data) {
					var res = JSON.parse(data);
					if (res.status == 'passed') {
						logForm.submit();
					} else {
						$('.message', logForm).html('Invalid User or password');
						return false;
					}
				},
				error: function (error) {
					return false;
				}
			});
			return false;
		});

	});
</script>